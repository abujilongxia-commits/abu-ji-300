// Generate prompt
export function generateExplosionPrompt(object: string, components: ExplosionComponent[], styleType: "realistic" | "diagram" | "technical"): string {
  const stylePrompts: Record<"realistic" | "diagram" | "technical", string> = {
    realistic: "Hyper-realistic style, professional studio lighting, soft shadow layers, high dynamic range, macro photography texture, ultra high resolution",
    diagram: "Infographic style, pure white background, clean lines, arrow indicators, professional labels, flat design, ultra high resolution",
    technical: "Technical exploded view, engineering drawing style, dimension lines, size annotations, technical terminology, professional CAD drafting standards, ultra high resolution",
  };

  const componentList = components.map(c => c.name).join(", ");

  return `Create a vertical exploded view infographic showing the decomposition of a ${object}. ${componentList} arranged floating from bottom to top, with subtle shadows and indicator lines marking each component. ${stylePrompts[styleType]}, DSLR macro photography style, delicate indicator lines, ultra clear details, ultra high quality, maximum resolution. Large bold English labels for each component name.`;
}
