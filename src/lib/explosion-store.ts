// 確保資料目錄存在
function ensureDataDir() {
  if (!fs.existsSync(DATA_DIR)) {
    fs.mkdirSync(DATA_DIR, { recursive: true });
  }
}

// 讀取索引
export function loadExplosionIndex(): ExplosionIndex {
  ensureDataDir();
  try {
    if (fs.existsSync(EXPLOSION_INDEX_FILE)) {
      const data = fs.readFileSync(EXPLOSION_INDEX_FILE, "utf-8");
      return JSON.parse(data);
    }
  } catch (error) {
    console.error("[ExplosionStore] 讀取索引失敗:", error);
  }
  return {
    generatedObjects: [],
    lastCronRun: null,
    totalGenerated: 0,
    cronEnabled: true,
  };
}

// 儲存索引
export function saveExplosionIndex(index: ExplosionIndex): void {
  ensureDataDir();
  try {
    fs.writeFileSync(EXPLOSION_INDEX_FILE, JSON.stringify(index, null, 2), "utf-8");
  } catch (error) {
    console.error("[ExplosionStore] 儲存索引失敗:", error);
  }
}

// 讀取圖庫
export function loadExplosionGallery(): ExplosionResult[] {
  ensureDataDir();
  try {
    if (fs.existsSync(EXPLOSION_GALLERY_FILE)) {
      const data = fs.readFileSync(EXPLOSION_GALLERY_FILE, "utf-8");
      return JSON.parse(data);
    }
  } catch (error) {
    console.error("[ExplosionStore] 讀取圖庫失敗:", error);
  }
  return [];
}

// 儲存圖庫
export function saveExplosionGallery(gallery: ExplosionResult[]): void {
  ensureDataDir();
  try {
    fs.writeFileSync(EXPLOSION_GALLERY_FILE, JSON.stringify(gallery, null, 2), "utf-8");
  } catch (error) {
    console.error("[ExplosionStore] 儲存圖庫失敗:", error);
  }
}

// 新增到圖庫
export function addToExplosionGallery(item: ExplosionResult): void {
  const gallery = loadExplosionGallery();
  gallery.unshift(item); // 新增到最前面
  saveExplosionGallery(gallery);
  console.log(`[ExplosionStore] 已新增到圖庫: ${item.objectName}`);
}

// 取得待生成的物件（尚未生成過的）
export function getPendingObjects(count: number = 40): string[] {
  const index = loadExplosionIndex();
  const pending: string[] = [];

  for (const obj of DEFAULT_OBJECTS) {
    if (!index.generatedObjects.includes(obj.toLowerCase())) {
      pending.push(obj);
      if (pending.length >= count) break;
    }
  }

  return pending;
}

// 標記物件為已生成
export function markObjectAsGenerated(objectName: string): void {
  const index = loadExplosionIndex();
  const key = objectName.toLowerCase();
  if (!index.generatedObjects.includes(key)) {
    index.generatedObjects.push(key);
    index.totalGenerated++;
    saveExplosionIndex(index);
  }
}

// 更新最後執行時間
export function updateLastCronRun(): void {
  const index = loadExplosionIndex();
  index.lastCronRun = new Date().toISOString();
  saveExplosionIndex(index);
}

// 檢查是否應該執行 Cron（每 20 小時）
export function shouldRunCron(): boolean {
  const index = loadExplosionIndex();
  if (!index.cronEnabled) return false;
  if (!index.lastCronRun) return true;

  const lastRun = new Date(index.lastCronRun);
  const now = new Date();
  const hoursSinceLastRun = (now.getTime() - lastRun.getTime()) / (1000 * 60 * 60);

  return hoursSinceLastRun >= 20;
}

// 啟用/停用 Cron
export function setCronEnabled(enabled: boolean): void {
  const index = loadExplosionIndex();
  index.cronEnabled = enabled;
  saveExplosionIndex(index);
}

